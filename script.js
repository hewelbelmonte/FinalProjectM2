function flipCard1() {
    var frontContent = document.getElementById("frontCard1");
    var backContent = document.getElementById("backCard1");

    if (frontContent.style.display === "block") {
    frontContent.style.display = "none";
    backContent.style.display = "block";
    } else {
    frontContent.style.display = "block";
    backContent.style.display = "none";
    }
}

